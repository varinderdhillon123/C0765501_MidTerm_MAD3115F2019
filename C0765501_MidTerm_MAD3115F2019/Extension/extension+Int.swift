import Foundation
extension Int{
     func concatUnits()->String
    {
        return "\(self) Units"
    }
     func conactGB() ->String
    {
        return " \(self) GB"
    }
    func concatMin() -> String
    {
        return " \(self) Minutes"
    }
    
    
}
